	public function filter(ShoppingCartStatusRequest $request)
	{
		if ($request->ajax()) {
			$type = $request->input('type', '');
			if ($type == 'dropdown') {
				$multiple = $request->input('multiple', 'false');
				$ids = $request->input('ids', '');
				$search = $request->input('search', '');

				if ($multiple == 'false') {
					$shoppingCartStatuses = ShoppingCartStatus::all();
					return response()->json($shoppingCartStatuses->toArray());
				}

				if ($ids != '') {
					$shoppingCartStatuses = ShoppingCartStatus::whereIn('id', $ids)->get();
				}
				if ($search != '') {
					$shoppingCartStatuses = ShoppingCartStatus::whereTranslationLike('name', '%'. $search .'%')->get();
				}
				
				return response()->json($shoppingCartStatuses->toArray());
			}

			$shoppingCartStatuses = ShoppingCartStatus::all();
			return response()->json($shoppingCartStatuses->toArray());
		}
	}
